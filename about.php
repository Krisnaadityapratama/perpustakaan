            <h2>Tentang PustakaStats UMRAH</h2>
            <p><strong>PustakaStats UMRAH</strong> adalah sebuah aplikasi berbasis web yang dirancang untuk membantu dalam analisis data pengunjung dan peminjaman buku di perpustakaan Universitas Maritim Raja Ali Haji (UMRAH). Aplikasi ini mengumpulkan, mengelola, dan menyajikan informasi mengenai perilaku pengunjung serta aktivitas peminjaman buku di perpustakaan UMRAH.</p>

            <h3>Tujuan Aplikasi</h3>
            <p>Aplikasi ini bertujuan untuk memberikan wawasan yang lebih dalam mengenai pola kunjungan dan peminjaman buku di perpustakaan UMRAH. Dengan menggunakan data yang terkumpul, pihak perpustakaan dapat membuat keputusan yang lebih baik terkait pengelolaan koleksi buku, serta meningkatkan layanan dan pengalaman pengunjung.</p>

            <h3>Fitur-Fitur PustakaStats UMRAH</h3>
            <ul>
                <li><strong>Analisis Data Pengunjung dan Peminjaman Berdasarkan Prodi:</strong> Menyediakan informasi terkait jumlah pengunjung dan peminjam berdasarkan program studi (Prodi) di UMRAH.</li>
                <li><strong>Analisis Data Pengunjung dan Peminjaman Berdasarkan Fakultas:</strong> Memberikan gambaran tentang jumlah pengunjung dan peminjam berdasarkan fakultas yang ada di UMRAH.</li>
                <li><strong>Grafik Interaktif:</strong> Menampilkan grafik batang untuk memvisualisasikan data kunjungan dan peminjaman buku dengan cara yang mudah dipahami.</li>
                <li><strong>Integrasi Algoritma Apriori:</strong> Menyediakan fitur untuk menganalisis pola hubungan antar buku yang sering dipinjam bersamaan menggunakan algoritma Apriori.</li>
                <li><strong>Manajemen Data:</strong> Fitur ini memungkinkan untuk memasukkan data pengunjung dan peminjaman buku secara manual melalui formulir yang ada di dalam aplikasi.</li>
            </ul>

            <h3>Cara Penggunaan Aplikasi</h3>
            <p>Pengguna dapat mengakses aplikasi ini melalui menu-menu yang ada di sidebar. Berikut adalah penjelasan singkat mengenai setiap menu:</p>
            <ul>
                <li><strong>Beranda:</strong> Halaman utama yang menampilkan informasi umum mengenai aplikasi.</li>
                <li><strong>Data Pengunjung & Peminjaman:</strong> Halaman untuk memasukkan dan melihat data pengunjung dan peminjaman buku.</li>
                <li><strong>Algoritma Apriori:</strong> Menampilkan hasil analisis pola peminjaman buku menggunakan algoritma Apriori.</li>
                <li><strong>About:</strong> Halaman ini yang berisi informasi tentang aplikasi PustakaStats UMRAH.</li>
            </ul>

